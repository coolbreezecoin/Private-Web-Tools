const packs: Record<GeneratedLocale, LocalePack> = { es, fr, de };

function cloneTokens(source: string, translated: string) {
  const tokens = source.match(/\{[^}]+\}/g) ?? [];
  return tokens.reduce((value, token) => (value.includes(token) ? value : `${value} ${token}`), translated);
}

function preserveUiValue(path: string[], value: string) {
  const key = path[path.length - 1] ?? "";
  if (key === "brand" || key === "icon" || key === "value") return true;
  if (/^(https?:\/\/|\/|\d|[A-Z0-9_.$<>{}\\[\]^+*?|()-]+$)/.test(value.trim())) return true;
  if (value.includes("\n") && /[{}[\]":,]/.test(value)) return true;
  return false;
}

function translateUiString(value: string, path: string[], pack: LocalePack) {
  const key = path[path.length - 1] ?? "";
  const block = path[0] ?? "";

  if (preserveUiValue(path, value)) return value;
  if (key === "title" && pack.uiTitles[block]) return pack.uiTitles[block];
  if (key === "privacy" && !["nav", "footer"].includes(block)) return pack.browserPrivacy;
  if (key === "description" && path.includes("pages")) return pack.exact[value] ?? pack.fallback(key, value);
  if (pack.exact[value]) return cloneTokens(value, pack.exact[value]);
  if (pack.labelByKey[key]) return pack.labelByKey[key];

  return cloneTokens(value, pack.fallback(key, value));
}

function translateObject(value: unknown, locale: GeneratedLocale, path: string[] = []): unknown {
  const pack = packs[locale];
  if (typeof value === "string") return translateUiString(value, path, pack);
  if (Array.isArray(value)) return value.map((item, index) => translateObject(item, locale, [...path, String(index)]));
  if (value && typeof value === "object") {
    return Object.fromEntries(Object.entries(value).map(([key, item]) => [key, translateObject(item, locale, [...path, key])]));
  }
  return value;
}

function makeToolContent(id: string, source: Record<string, unknown>, pack: LocalePack) {
  const copy = pack.toolCopy[id];
  if (!copy) throw new Error(`Missing localized tool copy for ${id}`);

  const translated: Record<string, unknown> = {
    name: copy.name,
    shortName: copy.shortName,
    description: `${copy.name}: ${copy.task}. ${pack.localResults}`,
    privacyNote: pack.browserPrivacy,
    hero: `${pack.usePrefix} ${copy.task}. ${pack.localResults}`,
    seo: {
      title: `${copy.name} - ${copy.seo}`,
      description: pack.seoDescription(copy.name, copy.task),
    },
    howTo: (source.howTo as unknown[]).map((_, index) => pack.howTo[index] ?? pack.howTo[pack.howTo.length - 1]),
    faq: (source.faq as unknown[]).map((_, index) => pack.faq[index] ?? pack.faq[pack.faq.length - 1]),
  };

  if (Array.isArray(source.body)) {
    translated.bodyTitle = copy.name;
    translated.body = [
      `${pack.usePrefix} ${copy.task}.`,
      pack.localProcessing,
    ].slice(0, source.body.length);
  }

  return translated;
}

function makeLandingContent(key: string, source: Record<string, unknown>, pack: LocalePack) {
  const copy = pack.landingCopy[key];
  if (!copy) throw new Error(`Missing localized landing copy for ${key}`);
  const bodyLength = Array.isArray(source.body) ? source.body.length : 0;
  const howToLength = Array.isArray(source.howTo) ? source.howTo.length : 0;
  const faqLength = Array.isArray(source.faq) ? source.faq.length : 0;

  return {
    keyword: copy.keyword,
    h1: copy.h1,
    intro: pack.landingIntro(copy.style),
    body: pack.landingBody(copy.style, bodyLength),
    seo: {
      title: `${copy.h1} - Unicode`,
      description: pack.landingSeoDescription(copy.style),
    },
    howTo: pack.landingHowTo(copy.style, howToLength),
    faq: pack.landingFaq(copy.style, faqLength),
  };
}

export function translateContent<T extends { tools: Record<string, unknown>; landings: Record<string, unknown> }>(
  source: T,
  locale: GeneratedLocale,
): T {
  const pack = packs[locale];
  return {
    tools: Object.fromEntries(
      Object.entries(source.tools).map(([id, value]) => [id, makeToolContent(id, value as Record<string, unknown>, pack)]),
    ),
    landings: Object.fromEntries(
      Object.entries(source.landings).map(([key, value]) => [key, makeLandingContent(key, value as Record<string, unknown>, pack)]),
    ),
  } as T;
}

export function translateUi<T>(source: T, locale: GeneratedLocale): T {
  return translateObject(source, locale) as T;
}
